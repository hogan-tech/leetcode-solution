# time complexity: O(1)
# space complexity: O(1)
import bisect
from typing import List


class Solution:
    def sampleStats(self, arr: List[int]) -> List[float]:
        minimum = float('inf')
        maximum = float('-inf')
        modeCount = float('-inf')
        totalCount = 0
        totalNum = 0
        mode = 0
        for num, count in enumerate(arr):
            minimum = min(minimum, num) if count > 0 else minimum
            maximum = max(maximum, num) if count > 0 else maximum
            totalCount += count
            totalNum += num * count
            if count > modeCount:
                modeCount = count
                mode = num

        mean = totalNum / totalCount

        for i in range(255):
            arr[i + 1] += arr[i]

        median1 = bisect.bisect(arr, (totalCount - 1) / 2)
        median2 = bisect.bisect(arr, totalCount / 2)
        median = (median1 + median2) / 2.0

        return [minimum, maximum, mean, median,  mode]


